#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;

	if (!s && *s != 0)
		return (0);
	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i++;
	}
	if ((char)c == s[i])
		return ((char *)s + i);
	return (0);
}
